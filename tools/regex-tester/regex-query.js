  var RegexQuery = {
    /**
     * 获取完整正则数据库
     * @returns {Array}
     */
    getDatabase: function () {
      return REGEX_DATABASE
    },

    /**
     * 获取所有分类名称
     * @returns {Array}
     */
    getCategories: function () {
      return REGEX_DATABASE.map(function (group) {
        return group.category
      })
    },

    /**
     * 根据分类获取正则列表
     * @param {string} category - 分类名称
     * @returns {Array}
     */
    getByCategory: function (category) {
      for (var i = 0; i < REGEX_DATABASE.length; i++) {
        if (REGEX_DATABASE[i].category === category) {
          return REGEX_DATABASE[i].items
        }
      }
      return []
    },

    /**
     * 搜索正则表达式
     * @param {string} keyword - 搜索关键词
     * @returns {Array} - 匹配的结果数组 [{category, name, pattern, desc, matchField}]
     */
    search: function (keyword) {
      if (!keyword || !keyword.trim()) {
        return []
      }

      var kw = keyword.trim().toLowerCase()
      var results = []

      for (var i = 0; i < REGEX_DATABASE.length; i++) {
        var group = REGEX_DATABASE[i]
        var category = group.category

        for (var j = 0; j < group.items.length; j++) {
          var item = group.items[j]
          var matched = false
          var matchField = ''

          // 按名称搜索
          if (item.name.toLowerCase().indexOf(kw) >= 0) {
            matched = true
            matchField = 'name'
          }
          // 按描述搜索
          else if (item.desc.toLowerCase().indexOf(kw) >= 0) {
            matched = true
            matchField = 'desc'
          }
          // 按正则内容搜索
          else if (item.pattern.toLowerCase().indexOf(kw) >= 0) {
            matched = true
            matchField = 'pattern'
          }
          // 按分类搜索
          else if (category.toLowerCase().indexOf(kw) >= 0) {
            matched = true
            matchField = 'category'
          }

          if (matched) {
            results.push({
              category: category,
              name: item.name,
              pattern: item.pattern,
              desc: item.desc,
              matchField: matchField
            })
          }
        }
      }

      return results
    },

    /**
     * 获取所有正则的总数
     * @returns {number}
     */
    getTotalCount: function () {
      var count = 0
      for (var i = 0; i < REGEX_DATABASE.length; i++) {
        count += REGEX_DATABASE[i].items.length
      }
      return count
    },

    /**
     * 获取分类统计
     * @returns {Array} [{category, count}]
     */
    getCategoryStats: function () {
      return REGEX_DATABASE.map(function (group) {
        return {
          category: group.category,
          count: group.items.length
        }
      })
    }
  }

  // 暴露到全局
  global.RegexQuery = RegexQuery

